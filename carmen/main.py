import argparse
import logging
import sys

import bottle
from turberfield.utils.misc import log_setup

from carmen import __version__

DEFAULT_PAUSE = 1.2
DEFAULT_DWELL = 0.3

@bottle.route("/")
def here():
    return "Hello World!"

@bottle.route("/call/<phrase:object>")
def call(phrase):
    return "Hello World!"

@bottle.route("/move/<location:object>")
def move(location):
    return "Hello World!"

def build_app():
    rv = Bottle()
    #app.router.add_filter("object", object_store)

    return rv

def main(args):
    log = logging.getLogger(log_setup(args, "carmen"))
    log.info("Starting server.")

    app = build_app()
    bottle.run(app, host="localhost", port=8080, debug=True)

def parser(description=__doc__):
    rv = argparse.ArgumentParser(
        description,
        fromfile_prefix_chars="@"
    )
    rv.add_argument(
        "--version", action="store_true", default=False,
        help="Print the current version number")
    rv.add_argument(
        "-v", "--verbose", required=False,
        action="store_const", dest="log_level",
        const=logging.DEBUG, default=logging.INFO,
        help="Increase the verbosity of output")
    rv.add_argument(
        "--log", default=None, dest="log_path",
        help="Set a file path for log output")
    rv.add_argument(
        "--pause", type=float, default=DEFAULT_PAUSE,
        help="Time in seconds [{0:0.3}] to pause after a line.".format(DEFAULT_PAUSE)
    )
    rv.add_argument(
        "--dwell", type=float, default=DEFAULT_DWELL,
        help="Time in seconds [{0:0.3}] to dwell on each word.".format(DEFAULT_DWELL)
    )
    rv.add_argument(
        "--db", required=False, default=None,
        help="Database URL.")
    return rv


def run():
    p = parser()
    args = p.parse_args()

    rv = 0
    if args.version:
        sys.stdout.write(__version__)
        sys.stdout.write("\n")
    else:
        rv = main(args)

    if rv == 2:
        sys.stderr.write("\n Missing command.\n\n")
        p.print_help()

    sys.exit(rv)


if __name__ == "__main__":
    run()
